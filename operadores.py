#Operações Matemáticas (Aritméticas)

"""
- soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisão inteira: //
- Resto da divisão: %
- Potência: **
"""
numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20//3)
print(20 % 3)
print (2 ** 3)

# Operadores Booleanos (operadores de comparação true or false)

idade1 = 23
idade2 = 30
altura1 = 1.68
altura2 = 1.65
altura3 = altura2

print (idade1 > idade2) #False
print (idade1 <= idade2) #True
print ('Python' == 'python') #False
print ('banana' != 'abacaxi') #True
print (altura1 >= altura2) #True
print (altura2 > altura3) #False

